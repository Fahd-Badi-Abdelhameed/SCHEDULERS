/******************************************************************************
* Description : Global Interrupt Enable (GIE) Function.		                  *
******************************************************************************/

void GIE_voidEnable(void){

	/* Enable global interrupt */
	SET_BIT(SREG, GIE);
	
}


/******************************************************************************
* Description : Global Interrupt Disable (GIE) Function.		              *
******************************************************************************/

void GIE_voidDisable(void){

	/* Disable global interrupt */
	CLR_BIT(SREG, GIE);
	
}



